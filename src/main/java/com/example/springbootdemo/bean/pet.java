package com.example.springbootdemo.bean;

public class pet {
    private String pname;
    private String ptype;
    private String psymptom;
    private String pvresult;
    private String pother;
    private String pdate;

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getPtype() {
        return ptype;
    }

    public void setPtype(String ptype) {
        this.ptype = ptype;
    }

    public String getPsymptom() {
        return psymptom;
    }

    public void setPsymptom(String psymptom) {
        this.psymptom = psymptom;
    }

    public String getPvresult() {
        return pvresult;
    }

    public void setPvresult(String pvresult) {
        this.pvresult = pvresult;
    }

    public String getPother() {
        return pother;
    }

    public void setPother(String pother) {
        this.pother = pother;
    }

    public String getPdate() {
        return pdate;
    }

    public void setPdate(String pdate) {
        this.pdate = pdate;
    }
}
